    public String[] getCourse(String afaculty)
    {
        String[] acourse = new String[2];
        if(afaculty.equalsIgnoreCase("School of Business"))
        {
            acourse[0] = "MSc in Marketing";
            acourse[1] = "MSc in Management";
        }
        
        if(afaculty.equalsIgnoreCase("School of Computing"))
        {
            acourse[0] = "MSc in Computer Networks";
            acourse[1] = "MSc in Computer Security";
        }
        return acourse;
    }
