    private void addXActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addXActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/logininfo","root","");
            String query = "insert into lecturerdetails(full_name,lecturer_id,e_mail,address,contact_no,birth_date,gender,faculty,for_year,course,subject,subject_code,qualification,institute,year)values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, fullnameX.getText());
            pst.setString(2, lecIdX.getText());
            pst.setString(3, emailX.getText());
            pst.setString(4, addressX.getText());
            pst.setString(5, contactnoX.getText());
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            birthdateX.setFormats("yyyy-MM-dd");
            String d = sdf.format(birthdateX.getDate());
            pst.setString(6, d);
            String gen;
            gen = genderX.getSelectedItem().toString();
            pst.setString(7, gen);
            String facul;
            facul = facultyX.getSelectedItem().toString();
            pst.setString(8, facul);
            String yr;
            yr = fyearX.getSelectedItem().toString();
            pst.setString(9, yr);
            String cou;
            cou = courseX.getSelectedItem().toString();
            pst.setString(10, cou);
            pst.setString(11, subjectX.getText());
            pst.setString(12, subIdX.getText());
            pst.setString(13, qualifiX.getText());
            pst.setString(14, instiX.getText());
            pst.setInt(15, Integer.parseInt(yearX.getText()));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Inserted Sucessfully!");

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_addXActionPerformed

